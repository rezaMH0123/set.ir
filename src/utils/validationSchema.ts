import { z } from "zod";

export const mobileSchema = z.object({
  mobile: z
    .string()
    .min(11, "شماره موبایل باید حداقل ۱۱ رقم باشد")
    .max(11, "شماره موبایل نباید بیشتر از ۱۱ رقم باشد")
    .regex(
      /^(0[0-9]{2,}[0-9]{8,})|(\u06F0[\u06F0-\u06F9]{2,}[\u06F0-\u06F9]{8,})$/,
      "لطفاً شماره موبایل معتبر وارد کنید"
    ),
});
